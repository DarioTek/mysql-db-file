CREATE TABLE IF NOT EXISTS NEWS_SENTIMENT (
    SYMBOL VARCHAR(100) NOT NULL,
    RECORD_DATE DATE,
    GOOGLE_NEWS_HEADLINES VARCHAR(10000),
	OVERALL_SENTIMENT VARCHAR(100),
    PRIMARY KEY (SYMBOL, RECORD_DATE)
);

